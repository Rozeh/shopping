package model;

import java.sql.Date;

public class AutoBean {

	private int autonum;
	private String autoname;
	private String autoinfo;
	private String autoimg;
	private int autoprice;
	private Date autodate;
	private String autotemp;
	private String autocategory;
		
	
	public int getAutonum() {
		return autonum;
	}
	public void setAutonum(int autonum) {
		this.autonum = autonum;
	}
	public String getAutoname() {
		return autoname;
	}
	public void setAutoname(String autoname) {
		this.autoname = autoname;
	}
	public String getAutoinfo() {
		return autoinfo;
	}
	public void setAutoinfo(String autoinfo) {
		this.autoinfo = autoinfo;
	}
	public String getAutoimg() {
		return autoimg;
	}
	public void setAutoimg(String autoimg) {
		this.autoimg = autoimg;
	}
	public int getAutoprice() {
		return autoprice;
	}
	public void setAutoprice(int autoprice) {
		this.autoprice = autoprice;
	}
	public Date getAutodate() {
		return autodate;
	}
	public void setAutodate(Date autodate) {
		this.autodate = autodate;
	}
	public String getAutotemp() {
		return autotemp;
	}
	public void setAutotemp(String autotemp) {
		this.autotemp = autotemp;
	}
	public String getAutocategory() {
		return autocategory;
	}
	public void setAutocategory(String autocategory) {
		this.autocategory = autocategory;
	}
	
	
}
